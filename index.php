					The part of my life that isn't devoted to making a living is devoted to making art. 
					I got my MFA from the Tyler School of Art in 2005; I'm a painter and sculptor. 
					While I use nearly any material I can get my hands on, I have an enduring love of oil paint, in part because of its incredible variety of very specific colors with long histories. 
